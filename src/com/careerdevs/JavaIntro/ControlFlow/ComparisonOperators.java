package com.careerdevs.JavaIntro.ControlFlow;

public class ComparisonOperators {

    public static void main(String[] args) {
        int x=1;
        int y =1;
        //Boolean Operator
        System.out.println(x == y);
        //Inequality Operator
        System.out.println(x != y);
        System.out.println(x <= y);
        System.out.println(x >= y);
    }
}
